package Graphe;

public class Graphes {

    private int suc;
    private int vois;

    public Graphes(int suc, int vois) {
        this.suc = suc;
        this.vois = vois;
    }

    public int getSuc() {
        return suc;
    }

    public void getVois(int vois) {
        return vois;
    }
}
