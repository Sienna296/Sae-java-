

public class Arete {
    private T dep;
    private T vois;
    private int val;

    public Arete(T dep, T vois, int val) {
        this.dep = dep;
        this.vois = vois;
        this.val = val;
    }
    public T getDep() {
        return dep;
    }
    public T getVois() {
        return vois;
    }

    public int getVal() {
        return val;
    }


}



